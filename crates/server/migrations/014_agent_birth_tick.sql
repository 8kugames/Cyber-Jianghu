-- ============================================================================
-- 014_agent_birth_tick.sql
-- ============================================================================
-- 为 agents 表添加 birth_tick 列，用于 Server 权威寿命计算
-- NULL = 不朽（迁移前创建的角色不受寿命约束）
-- ============================================================================

ALTER TABLE agents ADD COLUMN IF NOT EXISTS birth_tick BIGINT;

COMMENT ON COLUMN agents.birth_tick IS '角色出生 tick（秒级时间戳，不可变）。NULL = 不朽（迁移前角色）';
